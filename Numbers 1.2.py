"Вбудовані математичні функції для роботи з числами"
# 1. abs() - модуль числа
print(abs(-8))
print(abs(-4.78))

# 2. min() - знаходить мінімальне значення в рядку
print(min(2, -5, 45, -1, 0))
print(min(2.49, 9.393, 0.38, 4.3))

# 3. max() - знаходить максимальне значення в рядку
print(max(2, -5, 45, -1, 0))
print(max(2.49, 9.393, 0.38, 4.3))

# 4. pow() - зводить число в ступінь
print(pow(3, 5))
print(pow(2.4, 2))

# 5. round() - округлення числа
print(round(563.478))
print(round(457, -1))
print(round(589, -2))
print(round(3.457, 1))
print(round(3.457, 2))

# math - модуль з додатковими математичними функціями
import math
print(math.factorial(5))
print(math.pi)
print(math.sin(1))
