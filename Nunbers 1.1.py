"1. Числа"
# int - цілі числа
print(1, -4, 58, 456)

# float - дробові числа
print(2.3, 45.2, 2.47, -4.8)

# Операції з числами:
# 1. Додавання +
print(4+5)
print(3.2+4.3)

# 2. Віднімання -
print(78-3)
print(34.2-1.23)

# 3. Множення *
print(3*7)
print(2.5*4.3)

# 4. Ділення /
print(45/5)
print(7.8/23.2)

# 5. Цілісне ділення //
print(47//5)
print(59.4//3.4)

# 6. Залишок від ділення %
print(32 % 7)
print(34.3 % 3.8)

# 7. Піднесення до степення **
print(4**3)
print(2.7**5)
