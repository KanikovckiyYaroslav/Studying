"3.1 Bool Type"
# Логічний (булевий) тип даних використовує лише дві константи True і False

# Перетворення
print(bool(1))
print(bool(0))
print(bool("h"))
print(bool(""))

# Оператори порівняння
# 1. Менше
print(8 < 34)

# 2. Менше або дорівнює
print(7 <= 7)

# 3. Більше
print(34 > 21)

# 4. Більше або дорівнює
print(23 >= 4)

# 5. Дорівнює
print(45 == 45)

# 6. Не дорівнює
print(34 != 7)
